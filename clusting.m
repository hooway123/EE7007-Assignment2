R = zeros(16, 16);
for i = 1:1:16
    for j = 1:1:16
        A = x(i,1) * x(j,1) + x(i,2) * x(j,2) + x(i,3) * x(j,3) + x(i,4) * x(j,4);
        B = x(i,1) * x(i,1) + x(i,2) * x(i,2) + x(i,3) * x(i,3) + x(i,4) * x(i,4);
        C = x(j,1) * x(j,1) + x(j,2) * x(j,2) + x(j,3) * x(j,3) + x(j,4) * x(j,4);
        R(i, j) = abs(A)/(sqrt(B * C));
    end
end

pp = 0;
Result = maxmin(R, R);
while ~isequal(R, Result)
    pp = pp + 1;
    R = Result;
    Result = maxmin(R, R);
end
